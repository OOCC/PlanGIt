/* 有符号类型 */
#define INT8    char
#define INT16   short
#define INT32   int
#define INT64   long long
#define CHAR    char
#define LONG    long

/* 无符号类型 */
#define UINT8    unsigned char
#define UINT16   unsigned short
#define UINT32   unsigned int
#define UINT64   unsigned long long
#define UCHAR    unsigned char
#define ULONG    unsigned long

/* 其他类型 */
#define VOID	void

